          <div class="teifigure" style="width: 300px;"><a href="<?php echo($_ARCHON->PublicInterface->ImagePath); ?>/p138-atmosphericscience-900w.jpg"><img src="<?php echo($_ARCHON->PublicInterface->ImagePath); ?>/p138-atmosphericsciences-300w.jpg" width="300" height="242" alt="Using an early model desktop computer, ca. 1970s." title="Using an early model desktop computer, ca. 1970s." /></a><br /><span class="caption-head">Using an early model desktop computer, ca. 1970s.</span><br /><span class="caption-desc">Atmospheric Sciences Department Photographic Collection (P 138). (Click to enlarge)</span></div><br style="clear: both;" /></div>
        <p>SCARC staff regularly supervise both undergraduate and graduate students interested
          in working as unpaid interns within the department.  Internships can be designed for
          academic credit when requested and in collaboration with an intern's major professor.
          Internships are typically structured to last for one term, but may run longer depending
          on the project.
        </p>
        <p>In addition to the internship project itself, SCARC interns are required to:</p>
        <ul>
          <li>Maintain an internship log, including in it the experiences and challenges that they
            encountered over the course of their project;
          </li>
          <li>Complete an internship assessment and self-evaluation;</li>
          <li>Write at least one blog post about their project, for publication on <a href="http://wpmu.library.oregonstate.edu/osu_archives/">Speaking of History</a>, the <a href="http://wpmu.library.oregonstate.edu/oregon-multicultural-archives/">Oregon Multicultural Archives Blog</a> or the <a href="http://paulingblog.wordpress.com/">Pauling Blog</a>;
          </li>
          <li>Create a plan for next steps, if a project is not completed in the original scheduled
            duration of an internship.
          </li>
        </ul>
