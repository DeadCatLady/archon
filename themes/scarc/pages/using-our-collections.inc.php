        <h2>Reading Room Guidelines</h2><a name="reading-room-guidelines"></a><p>The Oregon State University Libraries Special Collections &amp; Archives Research Center
          is committed to facilitating the work of scholarly researchers. In order to protect
          and provide access to rare and valuable materials, SCARC requires all visitors to
          abide by rules and regulations of a more stringent nature than those of the general
          library. Patrons are required annually to complete a <a href="http://scarc.library.oregonstate.edu/researcher-registration.pdf">Researcher Registration Form</a> and provide picture identification upon each visit. Permission to examine SCARC materials
          will be granted to qualified researchers upon completion of the application form,
          and agreement to abide by the following rules and policies governing use of materials.
          Such permissions are granted subject to restrictions placed on the materials by the
          donors, depositors, SCARC staff, and/or related parties.
        </p>
        <h2>Use of Materials</h2><a name="use-of-materials"></a><p>A researcher is responsible for the safeguarding of any materials made available to
          him/her in SCARC Reading Room. Researchers will comply with the following guidelines
          or will be asked to leave the SCARC area.
        </p>
        <ol>
          <li>No food or drink or is allowed in the reading room.</li>
          <li>All personal items, excluding writing utensils, notepaper, laptops, and cameras, must
            be placed in the coat room or personal locker for security purposes. The SCARC staff
            is not responsible for the security of your personal property.
          </li>
          <li>All cell phones and other electronic devices must be silenced while in SCARC.</li>
          <li>Only pencils are permitted within SCARC.</li>
          <li>Adhesives including glue, tape, and post-it notes are prohibited in the reading room.</li>
          <li>Materials may not be marked, folded, or handled in any way likely to damage them.</li>
          <li>No more than one book or archival box may be accessed at a time.</li>
          <li>Protective gloves must be worn when examining photographic collections.</li>
          <li>No research materials may be removed from the reading room.</li>
          <li>All unbound materials (papers within folders and folders within boxes) must remain
            in the exact order received.
          </li>
          <li>Flash photography of SCARC materials is not allowed.</li>
        </ol>
        <h2>Works Cited / Rules for Citations</h2><a name="works-cited-/-rules-for-citations"></a><p>When citing materials that you have used in your work, please use the following format,
          replacing the information in brackets.
        </p>
        <blockquote>"Courtesy [Full name of Collection], Special Collections &amp; Archives Research Center,
          Oregon State University Libraries, Series [when applicable] Box [Box #]"
        </blockquote>
        <p>Example: "Courtesy Ava Helen and Linus Pauling Papers, Special Collections &amp; Archives
          Research Center, Oregon State University Libraries, Peace Box 6.001"
        </p>
