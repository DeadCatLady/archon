        <h2>Instruction and Outreach</h2><a name="instruction-and-outreach"></a><p></p>
        <p>The Special Collections and Archives Research Center has an active instruction program
          and encourage the use of its collections in undergraduate and graduate classes. Each
          year we host a diverse range of classes that use our collections for their research
          assignments or course work, with staff eager to introduce students and faculty to
          our unique archival, manuscript, and rare books collections. We offer a suite of options
          for instruction and orientation; our staff can offer informal instruction services
          such as research consultation sessions for students and faculty, general orientation
          sessions for classes doing original primary source research, and specialized sessions
          with hands-on examination of archival materials in a classroom setting.
        </p>
        <p>With strong collections pertaining to the history of science and technology; the historical
          records of OSU and papers of its prominent faculty members; collections documenting
          cultural and ethnic groups in Oregon; and collections documenting natural resources
          in the Pacific Northwest, especially agriculture and forestry, students can research
          a wide array of topics. In addition to traditional print documents, we also have fabulous
          photo collections, a wide variety of digital collections or exhibit resources, and
          a growing body of born digital records. Instructors are encouraged to work directly
          with our staff to choose the most relevant and appropriate materials for classes.
        </p>
        <p>Please read on below to learn more about the types of instruction offered and facilities
          that are available.
        </p>
        <h2>Types of Instruction</h2><a name="types-of-instruction"></a><ul>
          <li><span style="text-decoration: underline">Introduction to Special and Archival Collections</span>. This session includes basic information on what special collections and archives
            are and how primary source and printed material can be utilized for student research.
            This introductory session can be tailored to specific course interests.
          </li>
          <li><span style="text-decoration: underline">Topic Specific</span>. This type of session includes a more in-depth look at select topics relating to
            the individual course. Past instruction sessions have included lectures on the life
            and work of Linus and Ava Helen Pauling, medieval manuscripts and the history of the
            book, history of atomic energy, history of chemistry, history of biology, science
            and politics, public history in the American West, ethnic minorities in Oregon, and
            a variety of 20th century history classes.
          </li>
          <li><span style="text-decoration: underline">Consultations</span>.  These might include course planning sessions with faculty; student research planning;
            basic Records Management; and oral history basics.
          </li>
          <li><span style="text-decoration: underline">Facilities Tours</span>. This session includes a tour of the facilities and treasures of the 5th floor by
            one of the Special Collections &amp; Archives staff members. Learn about our gorgeous
            <a href="?f=facilities1">Douglas Strain Reading Room</a>, peek into <a href="?f=facilities1">Linus Pauling's office</a>, and explore the unique <a href="?f=facilities2">Wilson Room</a>. Other specialized tours include a tour of the <a href="coll/mcdonald/index.html">McDonald Rare Books Collection</a> or a tour of the <a href="coll/pauling/index.html">Ava Helen &amp; Linus Pauling Papers</a>. We welcome groups of all sizes and ages for tours, but ask that you contact us at
            least one week in advance to schedule a visit in order to accommodate staff schedules.
          </li>
        </ul>
