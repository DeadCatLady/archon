        <h2>Mission Statement</h2><a name="mission-statement"></a><p>The OSU Libraries Special Collections and Archives Research Center (SCARC) stimulates
          and enriches the research and teaching endeavors of Oregon State University through
          primary sources. As part of the University's land grant mission, SCARC makes these
          resources available to the OSU community, Oregonians, and the larger community of
          scholars and independent researchers. As the repository for and steward of the Libraries'
          rare and unique materials, we build distinctive and unique collections in our signature
          areas: natural resources, the history of science, university history, and Oregon's
          multicultural communities. These collections encompass manuscripts, archives, rare
          books, oral histories, photographs, ephemera, audio/visual materials, electronic and
          born digital records.
        </p>
        <p>To achieve this mission, the Special Collections and Archives Research Center:</p>
        <p>
        <ul>
          <li>Provides unique opportunities for teaching and learning</li>
          <li>Facilitates discovery through online access to SCARC collections</li>
          <li>Provides responsive, personable, and expert public service</li>
          <li>Assists communities in collecting, managing, preserving, and making their histories
            accessible
          </li>
          <li>Promotes access to and preservation of SCARC collections by collaborating within the
            OSU Libraries, the University, and regional primary resource communities
          </li>
          <li>Engages high achieving students through experiential learning opportunities</li>
        </ul>
        </p>
        <h2>Department History</h2><a name="department-history"></a><p>The Special Collections &amp; Archives Research Center was formed in 2011 with the merger
          of the Oregon State University Archives and the OSU Libraries Special Collections.
          The Oregon State University Archives was founded in 1961 with the intention of acquiring
          and preserving historical materials relating to the University.  The department was
          expanded in 1966 to include one of the first records management programs of its kind.
          After several locational and organizational changes, the University Archives joined
          the University Libraries on September 1, 2000.  A quarter century after the University
          Archives opened its doors, the OSU Libraries Special Collections was established as
          a repository for the University’s growing rare book and manuscript collections and
          officially received its first major accession that same year.  Over the following
          twenty-five years, the department grew to include a score of collections documenting
          the life and work of scientists and historians.
        </p>
        <p>Today, the Special Collections &amp; Archives Research Center spans the 3rd and 5th floors
          of the Valley Library, housing more than <a href="coll/index.html">1,000 archival collections</a> and tens of thousands of rare books and fine bindings.  SCARC is committed to promoting
          public access to the department’s holdings through tours, educational opportunities,
          and community outreach and continues to support and develop a robust, publicly-available
          web presence including <a href="http://oregonexplorer.info">Oregon Explorer</a>, the <a href="university-history.html">University History portal</a>, and <a href="http://pauling.library.oregonstate.edu">Linus Pauling Online</a>, among <a href="digital-resources.html">other resources</a>.
        </p>
        <p>For additional information about our location and the services available to patrons
          and researchers, please visit our <a href="facilities.html">Facilities</a> and <a href="services.html">Services</a> pages.  For any other questions, see our <a href="faq.html">FAQ</a>  or <a href="ask-an-archivist">contact us directly</a>.
        </p>
