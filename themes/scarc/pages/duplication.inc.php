        <h2>Reproduction &amp; Use</h2><a name="reproduction-&amp;-use"></a><p>The Special Collections &amp; Archives Research Center (SCARC) provides access to reproductions
          of its content as a means of facilitating research, publication, and scholarly discourse.
          Creation and use of reproductions made available through self-service copying, duplication
          by staff or other parties, and online resources including <a href="http://oregondigital.org/catalog">Oregon Digital</a> and <a href="http://flickr.com/photos/osucommons/">Flickr</a> are subject to the guidelines enumerated below.
        </p>
        <h2>Duplication</h2><a name="duplication"></a><p>SCARC provides researchers with digital access copies of materials under the scope
          of <a href="http://www.copyright.gov/title17/92chap1.html#107">fair use</a>. Any individual or organization conducting or requesting digitization of copyrighted
          materials is responsible for determining whether the subsequent use of this reproduction
          falls within the parameters of fair use.
        </p>
        <p>SCARC requires each item or group of items to be reviewed before reproductions can
          be made and maintains the right to limit or refuse reproduction of material if the
          act of duplication:
        </p>
        <ul>
          <li>Is in violation of restrictions placed by the item’s donor</li>
          <li>May result in physical damage to the item</li>
          <li>Cannot be completed due to the item’s physical size or format</li>
        </ul>
        <p>Upon approval, researchers may take images using SCARC’s self-service Bookeye scanner
          or a personal digital camera.
        </p>
        <p>Researchers unable to conduct research on site may request that access scans be created
          by SCARC staff. This service is limited to paper records and is dependent on staff
          availability. Turnaround time may exceed two weeks.
        </p>
        <p>SCARC may submit requests for high resolution scans or reproductions of non-paper
          formats including slides, glass plate negatives, and legacy audio/visual formats to
          outside vendors for fulfillment. Individuals or organizations requesting reproductions
          are subject to any fees assessed by that third party and will be notified of the estimated
          cost prior to duplication.
        </p>
        <p>All reproduction requests may be submitted to <a href="mailto:scarc@oregonstate.edu">scarc@oregonstate.edu</a></p>
        <h2>Publication, Display, &amp; Distribution</h2><a name="publication,-display,-&amp;-distribution"></a><p>SCARC collections include materials in three broad categories:</p>
        <ul>
          <li>Items in the <a href="http://fairuse.stanford.edu/overview/public-domain/welcome">public domain</a> – These may be used freely and without restrictions.
          </li>
          <li>Items for which the copyright is owned by Oregon State University – Please contact
            SCARC for permission to use.
          </li>
          <li>Items for which the copyright holder is an entity other than Oregon State University
            – It is the responsibility of the user to determine copyright status and seek permission
            from the rights owner(s) where appropriate.
          </li>
        </ul>
        <p>If an individual or entity intends to use copyrighted material in excess of fair use
          as determined by U.S. copyright law (<a href="http://www.copyright.gov/title17/92chap1.html">title 17, United States Code</a>), it is incumbent upon the user(s) to seek permission from the copyright holder(s)
          prior to use. Failure to do so may constitute copyright infringement.
        </p>
        <p>SCARC does not assess fees for use of any materials in its holdings or require users
          to receive permission from SCARC to publish, display or distribute materials whose
          copyright is held by an entity other than Oregon State University. However, SCARC
          asks that users seeking to publish, display, or distribute any item from the SCARC
          holdings notify SCARC using the <a href="http://scarc.library.oregonstate.edu/notification-of-use.pdf">Notification of Use</a> form. This enables SCARC to collect data necessary for enhancing user access.
        </p>
        <p>SCARC also asks that users accurately attribute the item in a caption, citation, or
          other appropriate format to the Oregon State University Libraries Special Collections
          &amp; Archives Research Center. The preferred attribution is “From the [Collection Name],
          OSU Libraries Special Collections &amp; Archives Research Center.” Guidelines for attribution
          can also be found on the Notification of Use form.
        </p>
        <p>SCARC can be contacted by email at <a href="mailto:scarc@oregonstate.edu">scarc@oregonstate.edu</a> or by phone at (541) 737-2075.
        </p>
