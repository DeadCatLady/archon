        <h2>Records Management</h2><a name="records-management"></a><p>The Records Management Program advises Oregon State University departments and offices
          on filing, storage, retention, re-formatting, and destruction of university records.
          The University Archivist has been designated as the Records Officer for Oregon State
          University and administers the Oregon University System <a href="http://arcweb.sos.state.or.us/pages/rules/oars_100/oar_166/166_475.html">Records Retention Schedule</a> for Oregon State University.
        </p>
        <p>The Records Management Program also provides storage and retrieval of non-permanent,
          in-active, and/or confidential records that have not met their retention period in
          the University Records Centers.  This service includes storage of materials in a secure
          location at no charge to OSU offices or departments as well as retrieval of needed
          materials from the records center by SCARC staff for delivery to your office.  Participating
          departments are responsible for providing standard records storage boxes (12x10x15
          inches), paying for transportation of materials to the records center (if necessary),
          and paying also for confidential destruction of materials once their retention period
          has been fulfilled.
        </p>
        <p>The Records Management Program was founded at OSU in 1966, making it one of the first
          programs of its kind. <span style="font-weight: bold">Please note that records management policies for Oregon State University are currently
                        undergoing revision</span>.
        </p>
        <p>For assistance with Records Management requests, please contact Elizabeth Nielsen,
          University Archivist and Records Center Manager (541-737-0543, <a href="mailto:elizabeth.nielsen@oregonstate.edu">elizabeth.nielsen@oregonstate.edu</a>).
        </p>
