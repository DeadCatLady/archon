        <h2>Reference</h2><a name="reference"></a><p>The Special Collections and Archives Research Center (SCARC) is open to all members
          of the Oregon State University community as well as the general public; however, our
          materials do not circulate and must be used in our Reading Room on the fifth floor
          of Valley Library. Reference assistance is provided by SCARC staff to help determine
          the materials appropriate for specific research projects. All materials are retrieved
          for the researcher by SCARC staff during our open hours (Monday and Wednesday-Friday
          from 10:00 am to 6:00 pm; Tuesday 10:00 am - 8:00 pm).
        </p>
        <p><span style="font-style: italic">What should I expect when I arrive?</span></p>
        <p>All researchers must register at the SCARC reference desk and are required to comply
          with the Reading Room policies, which are designed to protect archival materials for
          future use. Photo identification is required upon initial registration (OSU faculty,
          students, and staff may provide their OSU ID); you will be asked to show identification
          and sign in for each visit. For the safety of our materials and the ease of your research
          in our Reading Room, all personal property (including coats, bags, notebooks, books,
          and folders) must be left in the lockers/coatroom. Personal books and loose paper
          are allowed in some cases and must be approved by the desk staff.
        </p>
        <p>The Reading Room is a designated quiet area, so please keep your voices to a minimum.
          Additionally, because our space is limited those not using SCARC materials will be
          asked to find another study location in the library. Cell phone use is not permitted
          and we ask that you take calls outside the foyer as the space acts as a whisper gallery.
        </p>
